
public class DoWhileCounter {
	public static void main(String[]args) {
		//Use do while when you want to do
		//An operation at least 1 time
		int count = 1;
		do{
			System.out.printf("%d ", count);
			count++;
		}while(count <= 3);
		System.out.println();
		System.out.println("All Done!");
		
		
	}
}
